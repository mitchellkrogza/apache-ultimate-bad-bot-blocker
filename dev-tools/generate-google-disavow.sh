# **************************
# Create Google Disavow File
# **************************

sudo truncate -s 0 ./_google_webmaster_disavow_links/google-disavow.txt
while IFS= read -r LINE
do
printf '%s%s\n' "domain:" "${LINE}" >> ./_google_webmaster_disavow_links/google-disavow.txt
done < ${input1}

